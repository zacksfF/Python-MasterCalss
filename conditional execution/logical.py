# logical Operators
a = 10
b = 5
c = 20

result = (a > b) and (b < c)
print("(a > b) and (b < c):", result)  # Output: (a > b) and (b < c): True

result = (a < b) or (b > c)
print("(a < b) or (b > c):", result)  # Output: (a < b) or (b > c): False

result = not (a == b)
print("not (a == b):", result)  # Output: not (a == b): True
